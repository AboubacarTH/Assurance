    public AutomobileDialog(java.awt.Frame parent, boolean modal, Automobile automobile) {
        super(parent, modal);
        this.automobile = automobile;
        initComponents();
        id_tarif = this.automobile.getId_tarif();
        initCB();
        try {
            Tarif tarif = getController().getTarifController().getTarif(id_tarif);
            combo_classe.setSelectedItem(getController().getClasseController().getClasse(tarif.getId_classe()).getId());
            combo_groupe.setSelectedItem(getController().getGroupeController().getGroupe(tarif.getId_groupe()).getId());
            combo_zone.setSelectedItem(getController().getZoneController().getZone(tarif.getId_zone()).getZone());
            combo_zone.setSelectedItem(getController().getZoneController().getZone(tarif.getId_zone()).getZone());
            combo_usage.setSelectedItem(getController().getUsageController().getUsage(tarif.getId_usage()).getUsage());
            if (getController().getTarifController().getVehiculeController().getVehicule(tarif.getId()) != null) {
                radio_btn_vehicule.setSelected(true);
                combo_puissance.setSelectedItem(getController().getVehiculeMoteurController().getVehiculeMoteur(getController().getTarifController().getVehiculeController().getVehicule(tarif.getId()).getId_puissance()).getPuissance());
            } else {
                radio_btn_moto.setSelected(true);
                combo_puissance.setSelectedItem(getController().getMotoMoteurController().getMotoMoteur(getController().getTarifController().getMotoController().getMoto(tarif.getId()).getId_puissance()).getPuissance());
            }
            textField_tarif.setText("" + tarif.getTarif());
            textField_charge_utile.setText("" + this.automobile.getCharge_utile());
            textField_marque.setText(this.automobile.getMarque());
            textField_nombre_place.setText("" + this.automobile.getNombre_place());
            textField_numero_chassis.setText(this.automobile.getNumero_chassis());
            textField_numero_plaque.setText(this.automobile.getNumero_plaque());
            textField_type_auto.setText(this.automobile.getType());
            date_mise_circulation.setDate(this.automobile.getDate_circulation());
        } catch (Exception e) {
        }
    }

