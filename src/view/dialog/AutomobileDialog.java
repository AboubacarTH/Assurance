    private void ajouter_automobile() {
        if (id_tarif == 0 || textField_numero_plaque.getText().isBlank()) {
            warning_information();
            return;
        }
        int nombre_place;
        try {
            nombre_place = Integer.parseInt(textField_nombre_place.getText());
        } catch (NumberFormatException e) {
            erreur_information();
            return;
        }
        double charge;
        try {
            charge = Double.parseDouble(textField_charge_utile.getText());
        } catch (NumberFormatException e) {
            erreur_information();
            return;
        }
        java.sql.Date date = new java.sql.Date(date_mise_circulation.getDate().getTime());
        if (automobile == null) {
            if (getController().getAutomobileController().getAutomobile(textField_numero_plaque.getText()) != null) {
                erreur_information();
                return;
            }
            getController().getAutomobileController().addAutomobile(
                    id_tarif,
                    nombre_place,
                    date,
                    textField_type_auto.getText(),
                    textField_numero_plaque.getText(),
                    textField_marque.getText(),
                    textField_numero_chassis.getText(),
                    charge
            );
        } else {
            getController().getAutomobileController().updateAutomobile(
                    automobile.getId(),
                    id_tarif,
                    nombre_place,
                    date,
                    textField_type_auto.getText(),
                    textField_numero_plaque.getText(),
                    textField_marque.getText(),
                    textField_numero_chassis.getText(),
                    charge
            );
        }
        success_information();
        dispose();
    }

    private void prime_annuel() {
        if (combo_classe.getSelectedIndex() < 0 || combo_energie.getSelectedIndex() < 0 || combo_groupe.getSelectedIndex() < 0 || combo_puissance.getSelectedIndex() < 0 || combo_usage.getSelectedIndex() < 0 || combo_zone.getSelectedIndex() < 0) {
            return;
        }

        long id_classe;
        try {
            id_classe = getController().getClasseController().getClasse(Long.parseLong(combo_classe.getSelectedItem().toString())).getId();
        } catch (NumberFormatException e) {
            return;
        }

        long id_groupe;
        try {
            id_groupe = getController().getGroupeController().getGroupe(Long.parseLong(combo_groupe.getSelectedItem().toString())).getId();
        } catch (NumberFormatException e) {
            return;
        }

        long id_zone;
        Zone zone = getController().getZoneController().getZone(combo_zone.getSelectedItem().toString());
        if (zone == null) {
            return;
        }
        id_zone = zone.getId();
        long id_usage;
        Usage usage = getController().getUsageController().getUsage(combo_usage.getSelectedItem().toString());
        if (usage == null) {
            return;
        }
        id_usage = usage.getId();

        if (radio_btn_vehicule.isSelected()) {
            long id_energie;
            Energie energie = getController().getEnergieController().getEnergie(combo_energie.getSelectedItem().toString());
            if (energie == null) {
                return;
            }
            id_energie = energie.getId();
            long id_puissance_vehicule = getController().getVehiculeMoteurController().getVehiculeMoteur(combo_puissance.getSelectedItem().toString()).getId();
            Tarif tarif = getController().getTarifController().getTarif(id_classe, id_groupe, id_zone, id_usage, id_puissance_vehicule, id_energie);
            if (tarif != null) {
                id_tarif = tarif.getId();
                textField_tarif.setText("" + tarif.getTarif());
            } else {
                id_tarif = 0;
                textField_tarif.setText("");
            }

        } else {
            long id_puissance_moto = getController().getMotoMoteurController().getMotoMoteur(combo_puissance.getSelectedItem().toString()).getId();
            Tarif tarif = getController().getTarifController().getTarif(id_classe, id_groupe, id_zone, id_usage, id_puissance_moto);
            if (tarif != null) {
                id_tarif = tarif.getId();
                textField_tarif.setText("" + tarif.getTarif());
            } else {
                id_tarif = 0;
                textField_tarif.setText("");
            }
        }
    }

